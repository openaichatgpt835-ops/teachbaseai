export function coreModuleById(id: string) {
  return CORE_APP_MODULES.find((item) => item.id === id) || null;
}

export function coreModuleLabel(id: string, fallback = "") {
  return coreModuleById(id)?.label || fallback || id;
}

export function coreModuleDescription(id: string, fallback = "") {
  return coreModuleById(id)?.description || fallback;
}

export function coreModuleMeta(id: string, fallback?: Partial<CoreAppModule>) {
  const item = coreModuleById(id);
  return {
    id: item?.id || (fallback?.id as CoreAppModuleId | undefined) || (id as CoreAppModuleId),
    label: item?.label || fallback?.label || id,
    description: item?.description || fallback?.description || "",
    webPath: item?.webPath || fallback?.webPath,
    iframeTab: item?.iframeTab || fallback?.iframeTab,
    navGroup: item?.navGroup || fallback?.navGroup || "primary",
    iframeRequiresWebMode: item?.iframeRequiresWebMode ?? fallback?.iframeRequiresWebMode ?? false,
    loadingLabel: item?.loadingLabel || fallback?.loadingLabel || "",
    emptyTitle: item?.emptyTitle || fallback?.emptyTitle || "",
    emptyDescription: item?.emptyDescription || fallback?.emptyDescription || "",
  };
}

export function coreModuleLoadingLabel(id: string, fallback = "") {
  return coreModuleById(id)?.loadingLabel || fallback;
}

export function coreModuleEmptyState(id: string, fallbackTitle = "", fallbackDescription = "") {
  const item = coreModuleById(id);
  return {
    title: item?.emptyTitle || fallbackTitle,
    description: item?.emptyDescription || fallbackDescription,
  };
}

export function coreModulesForSurface(surface: AppSurface, options?: { isWebMode?: boolean }) {
  const isWebMode = !!options?.isWebMode;
  return CORE_APP_MODULES.filter((item) => {
    if (surface === "web") return !!item.webPath;
    if (!item.iframeTab) return false;
    if (item.iframeRequiresWebMode && !isWebMode) return false;
    return true;
  });
}

export function coreModulesByGroup(surface: AppSurface, group: ModuleNavGroup, options?: { isWebMode?: boolean }) {
  return coreModulesForSurface(surface, options).filter((item) => item.navGroup === group);
}
